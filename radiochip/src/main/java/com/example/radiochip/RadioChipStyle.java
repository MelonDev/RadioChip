package com.example.radiochip;

public class RadioChipStyle {

    private int backgroundColor = -1;
    private int iconColor = -1;
    private int activeIconColor = -1;
    private int ripperColor = -1;



    public int getRipperColor() {
        return ripperColor;
    }

    public void setRipperColor(int ripperColor) {
        this.ripperColor = ripperColor;
    }

    public int getActiveIconColor() {
        return activeIconColor;
    }

    public void setActiveIconColor(int activeIconColor) {
        this.activeIconColor = activeIconColor;
    }

    public int getBackgroundColor() {
        return backgroundColor;
    }

    public void setBackgroundColor(int backgroundColor) {
        this.backgroundColor = backgroundColor;
    }

    public int getIconColor() {
        return iconColor;
    }

    public void setIconColor(int iconColor) {
        this.iconColor = iconColor;
    }

}
